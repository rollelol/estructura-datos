package Clase_3;

public class Triangulo {


    public static void main(String[] args) {

        for (int i = 1; i<=1; i++){
            System.out.println("*");


            for (int j = 1; j<=1; j++){
                System.out.println("* *");
            }

            for (int j = 1; j<=1; j++ ){
                System.out.println("* * *");
            }

            for (int k = 1; k<=1; k++ ){
                System.out.println("* * * *");
            }


        }



    }

}
